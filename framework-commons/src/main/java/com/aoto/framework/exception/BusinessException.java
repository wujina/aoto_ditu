package com.aoto.framework.exception;

/**
 * [简要描述]:<br/>
 * [详细描述]:<br/>
 *
 * @author zongwj
 * @version 1.0, 2017年5月25日
 */
public class BusinessException extends Exception {

    private static final long serialVersionUID = -3333739870770525952L;
    
    /**
     * [简要描述]:<br/>
     * [详细描述]:<br/>
     *
     * @author zongwj
     */
    public BusinessException() {
        super();
    }

    /**
     * [简要描述]:<br/>
     * [详细描述]:<br/>
     *
     * @author zongwj
     * @param message String
     */
    public BusinessException(String message) {
        super(message);
    }

    /**
     * [简要描述]:<br/>
     * [详细描述]:<br/>
     *
     * @author zongwj
     * @param message String
     * @param cause Throwable
     */
    public BusinessException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * [简要描述]:<br/>
     * [详细描述]:<br/>
     *
     * @author zongwj
     * @param cause Throwable
     */
    public BusinessException(Throwable cause) {
        super(cause);
    }
}
